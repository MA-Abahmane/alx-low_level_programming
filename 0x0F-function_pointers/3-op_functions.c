/**
 * op_add - add up 2 numbers
 * @a: num 1
 * @b: num 2
 * Return: the resulte
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_add - subtract 2 numbers
 * @a: num 1
 * @b: num 2
 * Return: the resulte
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_add - multiply 2 numbers
 * @a: num 1
 * @b: num 2
 * Return: the resulte
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_add - devide 2 numbers
 * @a: num 1
 * @b: num 2
 * Return: the resulte
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_add - find the modulo of 2 numbers
 * @a: num 1
 * @b: num 2
 * Return: the modulo
 */
int op_mod(int a, int b)
{
return (a % b);
}
